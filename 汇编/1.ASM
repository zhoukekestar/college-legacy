CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	
	MOV AX, SEG DATA
	MOV DS, AX
	MOV BX, DS:[0006H]

	
	MOV DL,BH
	
	CALL PRINTBX

	
	AND BX,0FFH
	CALL PRINTBX

	OR BH,[BX]
	CALL PRINTBX


	MOV AX,4C00H
	INT 21H


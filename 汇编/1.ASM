	PRINTBX PROC
		MOV DL,BH
		ADD DL,30H
		MOV AH, 02H
		INT 21H

		MOV DL,BL
		ADD DL,30H
		MOV AH, 02H
		INT 21H 
		RET
	PRINTBX ENDP
